        [Test]
        public void TransformTriangles()
        {
            var triangles = new[]
            {
                new[] { 101, 301, 501 },
                new[] { 102, 302, 502 },
                new[] { 103, 303, 503 },
                new[] { 201, 401, 601 },
                new[] { 202, 402, 602 },
                new[] { 203, 403, 603 },
            };

            var transformedTriangles = validator.TransformTrianglesFromColumns(triangles);
            Assert.That(transformedTriangles[0], Is.EquivalentTo(new[] { 101, 102, 103 }));
            Assert.That(transformedTriangles[1], Is.EquivalentTo(new[] { 301, 302, 303 }));
            Assert.That(transformedTriangles[2], Is.EquivalentTo(new[] { 501, 502, 503 }));
            Assert.That(transformedTriangles[3], Is.EquivalentTo(new[] { 201, 202, 203 }));
            Assert.That(transformedTriangles[4], Is.EquivalentTo(new[] { 401, 402, 403 }));
            Assert.That(transformedTriangles[5], Is.EquivalentTo(new[] { 601, 602, 603 }));
            Assert.That(transformedTriangles.Count(), Is.EqualTo(triangles.Count()));
        }

